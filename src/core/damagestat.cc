#include "damagestat.h"
#include "trigger.h"

namespace Li {

void Damage_Stat::do_damage(const Update_Ctx &ui, Unit *self, real_t d)
{
  damage += d;

  if (auto *trigger = self->query_component(CID_Trigger)->as<Trigger>())
  {
    trigger->on_damage(ui, d);

    if (dead())
      trigger->on_damage_capacity(ui);
  }
}

} // namespace Li

